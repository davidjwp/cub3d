#include "cub3d.h"

void	print_map(char **split)
{
	for(int y = 0, i = 0; split[y]; y++){
		for(; split[y][i]; i++)
			split[y][i] == '\t' ? write (1, "    ", 4) : write (1, &split[y][i], 1);
		i = 0;
		if (split[y][i] != '\n')
			write (1, "\n", 1);
	}	
}

